"use client";

import { useEffect, useState } from "react";
import { createPortal } from "react-dom";

export default function Modal({ open, onClose, children }) {
  const [mounted, setMounted] = useState(false);

  // Avoid hydration mismatch (Next.js SSR)
  useEffect(() => {
    setMounted(true);
  }, []);

  // Close on ESC
  useEffect(() => {
    const handleEsc = (e) => {
      if (e.key === "Escape") onClose();
    };
    window.addEventListener("keydown", handleEsc);
    return () => window.removeEventListener("keydown", handleEsc);
  }, [onClose]);

  if (!mounted || !open) return null;

  return createPortal(
    <div
      className="fixed inset-0 z-50 flex items-center justify-center bg-black/50 backdrop-blur-sm"
      onClick={onClose}
    >
      <div className="relative">
        {/* Floating Close Button */}
        <button
          onClick={onClose}
          aria-label="Close modal"
          className="absolute -top-4 -right-4 w-10 h-10 flex items-center justify-center 
                     rounded-full bg-white shadow-lg border border-gray-200
                     hover:bg-gray-100 transition z-50"
        >
          <svg
            xmlns="http://www.w3.org/2000/svg"
            className="w-5 h-5 text-gray-700"
            fill="none"
            viewBox="0 0 24 24"
            strokeWidth="2"
            stroke="currentColor"
          >
            <path strokeLinecap="round" strokeLinejoin="round" d="M6 18L18 6M6 6l12 12" />
          </svg>
        </button>

        {/* Modal Content */}
        <div
          className="bg-white rounded-2xl shadow-xl w-full max-w-md p-6 animate-modal"
          onClick={(e) => e.stopPropagation()}
        >
          {children}
        </div>
      </div>
    </div>,
    document.body // ðŸ‘ˆ Portal target (IMPORTANT!)
  );
}